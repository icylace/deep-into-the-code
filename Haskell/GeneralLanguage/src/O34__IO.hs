module O34__IO (getInt, getThingy, nextOne, meToo, meTooIsm, bumpIt) where

getInt :: IO Int
getInt = fmap read getLine

{- GHCi ------------------------------------------------------------------------

> getInt
10
10

-------------------------------------------------------------------------------}

getThingy :: IO ()
getThingy = fmap (const ()) getInt

{- GHCi ------------------------------------------------------------------------

> getThingy
10

-------------------------------------------------------------------------------}

_ = const () getInt    -- `()`

_ = return 1 :: IO Int    -- `1`

_ = return () :: IO ()    -- No output.

nextOne :: IO Int
nextOne = fmap (+1) getInt

{- GHCi ------------------------------------------------------------------------

> nextOne
10
11

-------------------------------------------------------------------------------}

meToo :: IO String
meToo = fmap (++ " and me too!") getLine

{- GHCi ------------------------------------------------------------------------

> meToo
hello
"hello and me too!"

-------------------------------------------------------------------------------}

meTooIsm :: IO String
meTooIsm = do
  input <- getLine
  return (input ++ " and me too!")

{- GHCi ------------------------------------------------------------------------

> meTooIsm
hello
"hello and me too!"

-------------------------------------------------------------------------------}

bumpIt :: IO Int
bumpIt = do
  intVal <- getInt
  return (intVal + 1)

{- GHCi ------------------------------------------------------------------------

> bumpIt
10
11

-------------------------------------------------------------------------------}
