module O12_Lists where




-- A range of values can be created with some shorthand notation.

_ = [0..5]
-- Result will be `[0, 1, 2, 3, 4, 5]`.

_ = ['a'..'z']
-- Result will be `"abcdefghijklmnopqrstuvwxyz"`.








